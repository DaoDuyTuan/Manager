    private void viewAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAllActionPerformed
        // TODO add your handling code here:
        curatorTable.setRowCount(0);
        List<Curator> list = admin.viewDetailCurator();

        for (Curator curator : list) {
            curatorTable.addRow(new Object[]{curator.getCuratorID(),
                curator.getCuratorName(),curator.getCuratorAddress(),curator.gettotalStudent()});
        }
    }//GEN-LAST:event_viewAllActionPerformed

