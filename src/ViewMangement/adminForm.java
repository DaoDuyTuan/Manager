    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        curatorTable.setRowCount(0);
        String textSearch = txtSearch.getText();
        Curator cur = admin.searchCurator(textSearch);
        if(cur != null){
            curatorTable.addRow(new Object[]{cur.getCuratorID(),
                cur.getCuratorName(),cur.getCuratorAddress(),cur.gettotalStudent()});
        }
    }//GEN-LAST:event_searchActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:

        String name = txtName.getText();
        String pass = String.valueOf(txtPass.getPassword());
        String id = txtID.getText();
        String address = txtAddress.getText();
        String adminID = loginForm.adminID;

        if(admin.insertCurato(id, name, address, pass,adminID)){
            JOptionPane.showMessageDialog(this, "Insert successful!");
        }else{
            JOptionPane.showMessageDialog(this, "You entered wrong!");
        }
    }//GEN-LAST:event_addActionPerformed

