    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        Vector vt = (Vector) curatorTable.getDataVector().elementAt(jTable1.getSelectedRow());
        String id = (String) vt.elementAt(0);
        String name = (String) vt.elementAt(1);
        String address = (String) vt.elementAt(2);
        
        if(admin.updateCurator(id, name, address)){
            viewAll.doClick();
        }
    }//GEN-LAST:event_updateActionPerformed

