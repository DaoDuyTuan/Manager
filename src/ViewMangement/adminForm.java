    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        Vector vt = (Vector) curatorTable.getDataVector().elementAt(jTable1.getSelectedRow());
        String id = (String) vt.get(0);
        
        curatorTable.setRowCount(0);
        if(admin.deleteCuratorFromDB(id)){
            viewAllActionPerformed(evt);
        }else{
            JOptionPane.showMessageDialog(this, "You can't delete here!");
        }
    }//GEN-LAST:event_deleteActionPerformed

